def seed_clubs(db: Session, clear_existing: bool = False):
    """
    Seed the database with club data

    Args:
        db: Database session
        clear_existing: If True, delete all existing clubs first (WARNING: this will cascade delete memberships and potentially break relationships)
    """
    try:
        if clear_existing:
            print("‚ö†Ô∏è  Clearing existing clubs...")
            db.query(Club).delete()
            db.commit()
            print("‚úÖ Existing clubs cleared")

        print(f"\nüå± Seeding {len(CLUBS_DATA)} clubs...")

        clubs_added = 0
        clubs_skipped = 0

        for club_data in CLUBS_DATA:
            # Check if club already exists (by slug)
            existing_club = db.query(Club).filter(Club.slug == club_data["slug"]).first()

            if existing_club:
                print(f"‚è≠Ô∏è  Skipping '{club_data['name']}' - already exists")
                clubs_skipped += 1
                continue

            # Create new club
            club = Club(**club_data)
            db.add(club)
            clubs_added += 1
            print(f"‚úÖ Added: {club_data['name']} ({club_data['category'].value})")

        db.commit()

        print(f"\nüìä Summary:")
        print(f"   ‚úÖ Clubs added: {clubs_added}")
        print(f"   ‚è≠Ô∏è  Clubs skipped: {clubs_skipped}")
        print(f"   üìù Total clubs in database: {db.query(Club).count()}")

        # Show statistics by category
        print(f"\nüìà Clubs by category:")
        for category in ClubCategory:
            count = db.query(Club).filter(Club.category == category).count()
            total_members = db.query(Club).filter(Club.category == category).with_entities(
                func.sum(Club.member_count)
            ).scalar() or 0
            print(f"   {category.value.capitalize()}: {count} clubs ({total_members} total members)")

        # Show featured clubs
        featured_count = db.query(Club).filter(Club.is_featured == True).count()
        print(f"\n‚≠ê Featured clubs: {featured_count}")

        return clubs_added, clubs_skipped

    except Exception as e:
        db.rollback()
        print(f"\n‚ùå Error seeding clubs: {str(e)}")
        raise


def main():
    """Main function to run the seeding script"""
    print("=" * 60)
    print("ClubCompass - Database Seeding Script")
    print("=" * 60)

    # Check if user wants to clear existing data
    clear_existing = False
    if len(sys.argv) > 1 and sys.argv[1] == "--clear":
        response = input("\n‚ö†Ô∏è  WARNING: This will DELETE all existing clubs and related data!\nAre you sure? (yes/no): ")
        if response.lower() == "yes":
            clear_existing = True
        else:
            print("‚ùå Aborted")
            return

    # Create database session
    db = SessionLocal()

    try:
        # Ensure tables exist
        print("\nüîß Ensuring database tables exist...")
        Base.metadata.create_all(bind=engine)
        print("‚úÖ Database tables ready")

        # Seed clubs
        seed_clubs(db, clear_existing=clear_existing)

        print("\n‚ú® Database seeding completed successfully!")
        print("\nYou can now:")
        print("  1. Start the backend: uvicorn app.main:app --reload")
        print("  2. Start the frontend: npm run dev")
        print("  3. Visit http://localhost:3000 to see the clubs")

    except Exception as e:
        print(f"\nüí• Fatal error: {str(e)}")
        import traceback
        traceback.print_exc()
    finally:
        db.close()


if __name__ == "__main__":
    main()
